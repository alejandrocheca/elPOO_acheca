public abstract class Figura {
    static String colRelleno = "negro";
    static String colBorde = "blanco";
    

    public Figura(String colRelleno,String colBorde){
        this.colRelleno = colRelleno;
        this.colBorde = colBorde;
        
    }

    public String getFigura(){
        return getFigura();
    }
}
